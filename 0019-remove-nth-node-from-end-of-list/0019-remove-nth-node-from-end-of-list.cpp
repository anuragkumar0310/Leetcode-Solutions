class Solution {
public:
    ListNode* removeNthFromEnd(ListNode* head, int n) {
        ListNode *fast = head, *slow = head;
        int count = 0;
        
        while (count < n) {
            fast = fast->next;
            count++;
        }
        
        return (fast == nullptr) ? head->next : removeNode(fast, slow, head);
    }
    
private:
    ListNode* removeNode(ListNode* fast, ListNode* slow, ListNode* head) {
        while (fast->next != nullptr) {
            slow = slow->next;
            fast = fast->next;
        }
        
        slow->next = slow->next->next;
        
        return head;
    }
};
